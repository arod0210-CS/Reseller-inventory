  function getLocale(language) {
    return language === "es" ? "es-US" : "en-US";
  }

  function interpolate(template, variables) {
    return String(template).replace(/\{\{(.*?)\}\}/g, function (_, key) {
      return Object.prototype.hasOwnProperty.call(variables, key.trim()) ? variables[key.trim()] : "";
    });
  }

  function getDictionary(language) {
    return translations[language] || translations.en;
  }

  function applyTranslations(root, translate) {
    if (!root) {
      return;
    }

    root.querySelectorAll("[data-i18n]").forEach(function (element) {
      element.textContent = translate(element.getAttribute("data-i18n"));
    });

    root.querySelectorAll("[data-i18n-placeholder]").forEach(function (element) {
      element.setAttribute("placeholder", translate(element.getAttribute("data-i18n-placeholder")));
    });

    root.querySelectorAll("[data-i18n-aria-label]").forEach(function (element) {
      element.setAttribute("aria-label", translate(element.getAttribute("data-i18n-aria-label")));
    });

    root.querySelectorAll("[data-i18n-title]").forEach(function (element) {
      element.setAttribute("title", translate(element.getAttribute("data-i18n-title")));
    });
  }

  function createI18n(initialLanguage) {
    let language = translations[initialLanguage] ? initialLanguage : "en";

    function t(key, variables) {
      const dictionary = getDictionary(language);
      const fallback = translations.en[key] || key;
      const template = dictionary[key] || fallback;
      return interpolate(template, variables || {});
    }

    return {
      getLanguage: function () {
        return language;
      },
      setLanguage: function (nextLanguage) {
        language = translations[nextLanguage] ? nextLanguage : "en";
        return language;
      },
      t: t,
      apply: function (root) {
        applyTranslations(root, t);
      },
      formatCurrency: function (value) {
        if (value === null || value === undefined || value === "" || Number.isNaN(Number(value))) {
          return "--";
        }

        return new Intl.NumberFormat(getLocale(language), {
          style: "currency",
          currency: "USD",
          minimumFractionDigits: 0,
          maximumFractionDigits: 2
        }).format(Number(value));
      },
      formatNumber: function (value) {
        return new Intl.NumberFormat(getLocale(language), {
          maximumFractionDigits: 0
        }).format(Number(value) || 0);
      },
      formatPercent: function (value) {
        if (value === null || value === undefined || !Number.isFinite(Number(value))) {
          return "--";
        }

        return new Intl.NumberFormat(getLocale(language), {
          style: "percent",
          minimumFractionDigits: 0,
          maximumFractionDigits: 1
        }).format(Number(value));
      },
      formatDate: function (value, options) {
        if (!value) {
          return "--";
        }

        return new Intl.DateTimeFormat(getLocale(language), options || {
          month: "short",
          day: "numeric",
          year: "numeric"
        }).format(new Date(value));
      },
      formatDateTime: function (value) {
        if (!value) {
          return "--";
        }

        return new Intl.DateTimeFormat(getLocale(language), {
          month: "short",
          day: "numeric",
          year: "numeric",
          hour: "numeric",
          minute: "2-digit"
        }).format(new Date(value));
      },
      formatDateInput: function (value) {
        if (!value) {
          return "";
        }

        const date = new Date(value);
        if (Number.isNaN(date.getTime())) {
          return "";
        }

        const month = String(date.getMonth() + 1).padStart(2, "0");
        const day = String(date.getDate()).padStart(2, "0");
        return [date.getFullYear(), month, day].join("-");
      }
    };
  }

  global.PalletFlowI18n = {
    translations: translations,
    createI18n: createI18n,
    applyTranslations: applyTranslations
  };
